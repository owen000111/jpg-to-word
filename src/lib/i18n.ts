export function getTranslation(lang: Language, key: keyof typeof translations.zh, params?: Record<string, string | number>): string {
  const dict = (translations as any)[lang] || translations.zh;
  let text = (dict && dict[key]) || translations.zh[key];
  
  if (params) {
    Object.entries(params).forEach(([param, value]) => {
      text = text.replace(`{${param}}`, String(value));
    });
  }
  
  return text;
}


